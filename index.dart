// Todo archivo de flutter va dentro de un void main()
void mainBool() {
  print('Programa para saber si esta soleado');
  bool estaSoleado = true;
  print(estaSoleado);
}

void mainNumber() {
  print('Programa para saber la edad y estatura');
  int edad = 20;
  print('Edad $edad');
  double estatura = 1.65;
  print('Estatura $estatura');
}

